#!/usr/bin/env node

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { ENVIRONMENTS } from '../env/schema.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT_DIR = path.join(__dirname, '..');

const envName = process.argv[2] || process.env.ENV_NAME || 'local';

if (!Object.values(ENVIRONMENTS).includes(envName)) {
  console.error(`❌ Invalid environment: ${envName}`);
  console.error(`   Valid environments: ${Object.values(ENVIRONMENTS).join(', ')}`);
  process.exit(1);
}

console.log(`🔧 Generating backend wrapper env for: ${envName.toUpperCase()}`);

const envFilePath = path.join(ROOT_DIR, 'env', `env.${envName}.js`);
let envConfig;

try {
  const envModule = await import(`file://${envFilePath}`);
  envConfig = envModule.default;
} catch (err) {
  console.error(`❌ Failed to load environment config: ${envFilePath}`);
  console.error('   Error:', err.message);
  process.exit(1);
}

const secretsFilePath = path.join(ROOT_DIR, 'env', `env.${envName}.secrets.js`);
try {
  const secretsModule = await import(`file://${secretsFilePath}`);
  const secrets = secretsModule.default || {};
  envConfig = {
    ...envConfig,
    ...secrets,
    FEATURE_FLAGS: {
      ...(envConfig.FEATURE_FLAGS || {}),
      ...(secrets.FEATURE_FLAGS || {})
    }
  };
  console.log(`🔐 Secrets overlay loaded: env.${envName}.secrets.js`);
} catch {
  // optional
}

const wrapperPort = String(envConfig.WRAPPER_PORT || process.env.WRAPPER_PORT || '4444');
const mongoUri = String(envConfig.MONGO_URI || process.env.MONGO_URI || 'mongodb://localhost:27017');
const mongoDb = String(envConfig.MONGO_DB || process.env.MONGO_DB || 'impact');
const mongoExportBin = String(envConfig.MONGOEXPORT_BIN || process.env.MONGOEXPORT_BIN || '').trim();

const wrapperEnvLines = [
  '# Auto-generated by scripts/generate-backend-env.js',
  `# Environment: ${envName}`,
  `PORT=${wrapperPort}`,
  `MONGO_URI=${mongoUri}`,
  `MONGO_DB=${mongoDb}`
];

if (mongoExportBin) {
  wrapperEnvLines.push(`MONGOEXPORT_BIN=${mongoExportBin}`);
}

const wrapperEnvContent = `${wrapperEnvLines.join('\n')}\n`;
const wrapperEnvPath = path.join(ROOT_DIR, 'graphql-wrapper', '.env');
fs.writeFileSync(wrapperEnvPath, wrapperEnvContent, 'utf8');

console.log('✅ Backend wrapper env generated:');
console.log(`   File: ${wrapperEnvPath}`);
console.log(`   PORT: ${wrapperPort}`);
console.log(`   MONGO_URI: ${mongoUri}`);
console.log(`   MONGO_DB: ${mongoDb}`);
if (mongoExportBin) {
  console.log(`   MONGOEXPORT_BIN: ${mongoExportBin}`);
}

console.log('\n✨ Done!');
